# "min" va "max" metod yoki "funksiya"lari haqida
sonlar = list(range(1, 101)) # bu yerda "range" funksiyasidan foydalanildi
print(min(sonlar)) # "min" bu o'zgaruvchini ichidagi sonli elementni eng kichigini aniqlaydi
print(max(sonlar)) # "max" bu o'zgaruvchini ichidagi sonli elementni eng kattasini aniqlaydi