# "in" va "not in" haqida qisqacha
son = list(range(1, 99))
100 in son # "in" bormi? degan manoni anglatadi desa ham bo'ladi
    # yuqorida so'rov yuborilganda "False" ya'ni 'Yo'q' degan javob qaytaradi
      # ya'ni aniqroq qilib aytilsa < son > o'zgaruvchisin ichida 100 raqami yo'q
199 not in son # "not in" bu yo'qmi? degan manoni anglatadi desa ham bo'ladi
    # yuqorida so'rov yuborilganda "True" degan javob qaytaradi
      # ya'ni aniqroq qilib aytilsa < son > o'zgaruvchisini ichida 199 raqami yo'q